basket = ["a","c","e","b","d"]
#sort
basket.sort()
print(basket)

#sorted() sorts and return as new list
basket = ["x","c","e","b","d", "a", "y"]
print(sorted(basket))
print(basket)

new_basket = sorted(basket)
print(new_basket)

#reverse
new_basket.reverse()
print(new_basket)

print(new_basket[::-1])

